// honesty.js — Принцип на 99% честност (НИКОГА не лъжи).
//
// Това е изричен модул-принцип, който отговарящата логика ЗАДЪЛЖИТЕЛНО спазва:
//   1) Всеки отговор е заземен в (а) локалната памет, (б) цитиран източник, или
//      (в) детерминистично изчисление (математика). Иначе ботът казва „не знам“.
//   2) AI-генерирани предположения СЕ МАРКИРАТ ясно като предположение (не като факт).
//   3) Запазените факти СЕ МАРКИРАТ като наш спомен/източник.
//   4) Никога не измисляме източници или факти. Ако източникът липсва — не цитираме.
//
// Защо „99%, не 100%“: безплатният AI слой (Pollinations) е външен модел и може да
// сгреши. Затова неговите изходи минават през този модул и се представят като
// ПРЕДПОЛОЖЕНИЕ, а не като истина. Стопроцентова гаранция няма при езиков модел —
// честното е да го признаем и да маркираме несигурността, вместо да я скрием.

// Изрични фрази за „не знам“ — използват се вместо измисляне.
export const IDK = [
  'Не знам това със сигурност. Не искам да гадая.',
  'Не съм сигурен и не желая да измислям. Научи ме или ми дай задача да го проуча.',
  'Честно — нямам това в паметта си и нямам източник. Не знам.'
];

export function dontKnow(topic) {
  const base = IDK[Math.floor(Math.random() * IDK.length)];
  if (topic) return base + ` Мога да проуча „${topic}“, ако ми дадеш задача.`;
  return base;
}

// Етикет за източник на отговора (показва се в UI и в текста при нужда).
// kind: 'memory' | 'source' | 'math' | 'ai' | 'rule'
export function provenanceLabel(kind) {
  switch (kind) {
    case 'memory': return 'от паметта ми (нещо, на което ме научи)';
    case 'source': return 'от проучен източник';
    case 'math': return 'изчислено стъпка по стъпка';
    case 'ai': return 'ПРЕДПОЛОЖЕНИЕ от AI (непроверено)';
    case 'rule': return 'вградено правило';
    default: return 'неизвестен източник';
  }
}

// Обвива AI-изход така, че НИКОГА да не звучи като потвърден факт.
// Това гарантира маркиране на предположенията (точка 2 от принципа).
export function frameAiSuggestion(text) {
  const t = String(text || '').trim();
  if (!t) return null;
  return '🤔 Това е предположение (не съм го проверил): ' + t +
    '\n\nАко искаш сигурност, дай ми задача да го проуча от източник.';
}

// Обвива заземен в памет/източник отговор с честна атрибуция (по избор).
export function frameGrounded(text, kind, citation) {
  const t = String(text || '').trim();
  let out = t;
  if (citation) out += `\n\n📎 Източник: ${citation}`;
  return out;
}

// Валидатор: дали даден „факт“ има позволено заземяване. Помага да не пропускаме
// негрундирани твърдения в кода (използва се от tasks/responder при запис).
export function isGrounded(record) {
  if (!record) return false;
  // запис от памет винаги е „заземен“ — собственикът сам го е въвел или сме цитирали източник
  if (record.type === 'fact' || record.type === 'qa' || record.type === 'pref') return true;
  if (record.type === 'subject') return !!record.source; // ученето пази source
  return false;
}

